#1-序列切片注意事项
names = [x for x in range(1,11)]
#切片中最左边索引比它右边的晚出现在序列中，返回空,前提是步长为正
#左边索引与右边索引若与步长方向不一致，则为空
names[-3:0]
#包含最右侧
names[-3:]
#不包含最右侧
names[:3]
#包含所有
names[:]
#步长不能为0
names[::0]
#此处返回为空
names[10:0]
#没有10号索引元素，为何可以出现10号？
names[10:0:-2]
#从右往左，步长为2
names[::-2]
#从6开始，从右往左，步长为2
names[5::-2]

'''
总结：最左边元素包含在结果中，最右边元素不在分片内，步长为负数，开始元素大于结束元素
建议：在交互命令行中验证为主，以上规律不能涵盖所有情况
'''

