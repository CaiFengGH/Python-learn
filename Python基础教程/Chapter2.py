#3-列表的独有操作
#赋值：元组和字符串不具备赋值的功能
numbers = [1,5]
#替换切片，返回为[1,2,3,4,5]
numbers[1:1] = [2,3,4]
#返回为[1,5]
numbers[1:4] = []

#extend和'+'区别:extend方法修改被扩展的序列，'+'返回一个新的列表
a = [1,2,3]
b = [4,5,6]
a + b
a.extend(b)

#切片实现insert方法
numbers = [1,2,3,4,5]
numbers.insert(3,'haha')
numbers[3:3] = 'haha'

#改变列表却不返回值的方法:append remove reverse sort
#获取sort排序后的副本，且保留原有列表不变
#此时y为空，x排序
x = [3,5,4,2]
y = x.sort()
#y排序erx未排序
x = [3,5,4,2]
y = x[:]
y.sort()
#'=':x将引用赋值给了y,则两者都发生排序
x = [3,5,4,2]
y = x
y.sort()

